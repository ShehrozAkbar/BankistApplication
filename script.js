// these 2 functions are to calculate and format the date to use them in the movements function and the other place where date is shown
const calcdaypassed = function (curDate, transDate) {
  return Math.round((curDate - transDate) / (1000 * 60 * 60 * 24));
};
const movDateCalc = function (date) {
  const dayPassed = calcdaypassed(new Date(), date);
  if (dayPassed === 0) {
    return 'today';
  }
  if (dayPassed === 1) {
    return 'yesterday';
  }
  if (dayPassed > 1 && dayPassed < 8) {
    return `${dayPassed} Days ago`;
  }
  const now = new Date(date);
  const day = `${now.getDate()}`.padStart(2, 0);
  const month = `${now.getMonth()}`.padStart(2, 0);
  const year = now.getFullYear();
  return `${day}/${month}/${year}`;
};
// reset the timer function which will reset after everylogin and action
const resetTimer = function () {
  let tick = function () {
    let sec = String(time % 60);
    let min = String(Math.trunc(time / 60));
    labelTimer.textContent = `${min.padStart(2, 0)} : ${sec.padStart(2, 0)}`;
    // When 0 seconds, stop timer and log out user

    if (time === 0) {
      clearInterval(timer);
      labelWelcome.textContent = 'Log in to get started';
      containerApp.style.opacity = 0;
    }
    time--;
  };
  // Set time to 5 minutes
  let time = 120;
  // Call the timer every second

  tick();
  const timer = setInterval(tick, 1000);
  return timer;
};

// this function is to display all the movements
const displayMovements = function (acc, sort = false) {
  containerMovements.innerHTML = '';

  const movs = sort
    ? acc.movements.slice().sort((a, b) => a - b)
    : acc.movements;

  movs.forEach(function (mov, i) {
    const type = mov > 0 ? 'deposit' : 'withdrawal';

    const transferDate = movDateCalc(new Date(acc.movementsDates[i]));
    const html = `
      <div class="movements__row">
        <div class="movements__type movements__type--${type}">${
      i + 1
    } ${type}</div>
    <div class="movements__date">${transferDate}</div>
        <div class="movements__value">${mov.toFixed(2)}€</div>
      </div>
    `;

    containerMovements.insertAdjacentHTML('afterbegin', html);
  });
};

const calcDisplayBalance = function (acc) {
  acc.balance = acc.movements.reduce((acc, mov) => acc + mov, 0);
  labelBalance.textContent = `${acc.balance}€`;
};

const calcDisplaySummary = function (acc) {
  const incomes = acc.movements
    .filter(mov => mov > 0)
    .reduce((acc, mov) => acc + mov, 0);
  labelSumIn.textContent = `${incomes.toFixed(2)}€`;

  const out = acc.movements
    .filter(mov => mov < 0)
    .reduce((acc, mov) => acc + mov, 0);
  labelSumOut.textContent = `${Math.abs(out.toFixed(2))}€`;

  const interest = acc.movements
    .filter(mov => mov > 0)
    .map(deposit => (deposit * acc.interestRate) / 100)
    .filter((int, i, arr) => {
      // console.log(arr);
      return int >= 1;
    })
    .reduce((acc, int) => acc + int, 0);
  labelSumInterest.textContent = `${interest.toFixed(2)}€`;
};

const createUsernames = function (accs) {
  accs.forEach(function (acc) {
    acc.username = acc.owner
      .toLowerCase()
      .split(' ')
      .map(name => name[0])
      .join('');
  });
};
createUsernames(accounts);

const updateUI = function (acc) {
  // Display movements
  displayMovements(acc);

  // Display balance
  calcDisplayBalance(acc);

  // Display summary
  calcDisplaySummary(acc);
};

